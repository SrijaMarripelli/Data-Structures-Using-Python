# creating an array 
my_array = [1,2,3,4,5]

# access an element of the array
print(my_array[0])

# update an elemnt of the array
my_array[0]=6
print(my_array)

# append an element to the array
my_array.append(6)
print(my_array)

# get the length of the array
print(len(my_array))

# remove an element from the array
my_array.remove(4)
print(my_array)

# sort the array
my_array.sort()
print(my_array)